


class PointMass:

    def __init__(self ,m , x, y,):

        self.m = m

        self.x = x # x position
        self.y = y # y position

        self.fx = 0;
        self.fy = 0;

        self.vx = 0 # x velocity
        self.vy = 0 # y velocity

        self.ax = 0 # x acceleration
        self.bx = 0 # b acceleration


